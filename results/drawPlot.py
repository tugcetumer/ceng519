import pandas as pd
import matplotlib.pyplot as plt
data= pd.read_csv("/results.csv")

times = []
node_counts = []

time_avg = 0
for i in range(700):
  time_avg+= data.CompileTime[i]
  time_avg+= data.KeyGenerationTime[i]
  time_avg+= data.EncryptionTime[i]
  time_avg+= data.ExecutionTime[i]
  time_avg+= data.DecryptionTime[i]
  time_avg+= data.ReferenceExecutionTime[i]
  
  if i%100 == 99:
    time_avg /= 100
    times.append(time_avg)
    node_counts.append(data.NodeCount[i])
    time_avg = 0
    

print(times)
print(node_counts)


fig, ax = plt.subplots()

ax.stem(node_counts, times)

ax.set(ylim=(70, 75))

ax.set_ylabel('Average Total Time')
ax.set_xlabel('Node Count')

ax.set_xticks(node_counts)

for i, j in zip(node_counts, times):
   plt.text(i-1, j+0.2, '{:.2f}'.format(j))

plt.savefig('/deney/benchmark.png')
plt.show()